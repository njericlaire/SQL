--LOCAL- CAN ONLY BE ACCESSED WITHIN THE SAME CONNECTION AS IT WAS CREATED. GET DROPPED AUTOMATICALLY WHEN YOU CLOSE THE WINDOW OR CONNECTION
--GLOBAL- ARE AVAILABLE ACROSS CONNECTION OR QUERY WINDOW


