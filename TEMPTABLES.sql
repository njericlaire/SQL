---TEMP TABLES ARE STORED INSIDE THE SYSTEM DATABASES INSIDE THE DATABASE CALLED TEMPDB
--THEY HAVE THE FOLLOWING BENEFITS:
--CAN BE USED TO STORE TEMPORARY RESULTS

--CREATE TEMP TABLES
CREATE TABLE #EMPLOYEES(--IT WILL ALWAYS GO TO TEMPDB IF THERE IS A #
EMPID INT,
EMPNAME VARCHAR(100)
)
--REFER TI THE TABLE
SELECT * FROM #EMPLOYEES

